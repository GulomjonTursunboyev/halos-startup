# -*- coding: utf-8 -*-
"""Fix remaining text encoding issues"""

import sys
sys.stdout.reconfigure(encoding='utf-8')

with open('app/handlers.py', 'rb') as f:
    raw = f.read()

# Text-level fixes as bytes
# Cyrillic text corrupted patterns
fixes = [
    # Russian phrases (Windows-1251 double-encoded)
    (b'\xd0\xa0\xc2\xa7\xd0\xa0\xc2\xb8\xd0\xa1\xe2\x80\xa1\xd0\xa0\xd0\x85\xd0\xa1\xe2\x80\xb9\xd0\xa0\xc2\xb5 \xd0\xa0\xd2\x91\xd0\xa0\xd1\x95\xd0\xa0\xc2\xbb\xd0\xa0\xd1\x96\xd0\xa0\xd1\x91', 
     'Личные долги'.encode('utf-8')),
    (b'\xd0\xa0\xc5\x92\xd0\xa1\xe2\x82\xac\xd0\xa0\xc2\xb5\xd0\xa0\xd2\x91\xd0\xa0\xd1\x91\xd0\xa1\xe2\x80\x9a\xd0\xa1\xe2\x80\xb9',
     'Кредиты'.encode('utf-8')),
    (b'\xd0\xa0\xe2\x80\x9d\xd0\xa0\xd1\x95\xd0\xa0\xc2\xb1\xd0\xa0\xc2\xb0\xd0\xa0\xd0\x86\xd0\xa0\xd1\x91\xd0\xa1\xe2\x80\x9a\xd0\xa1\xc5\x92 \xd0\xa0\xd2\x91\xd0\xa0\xd1\x95\xd0\xa0\xc2\xbb\xd0\xa0\xd1\x96',
     'Добавить долг'.encode('utf-8')),
    (b'\xd0\xa0\xe2\x80\x94\xd0\xa0\xc2\xb0\xd0\xa0\xd1\x96\xd0\xa1\xe2\x82\xac\xd0\xa1\xc6\x92\xd0\xa0\xc2\xb7\xd0\xa0\xd1\x91\xd0\xa1\xe2\x80\x9a\xd0\xa1\xc5\x92 KATM',
     'Загрузить KATM'.encode('utf-8')),
    
    # Plus sign
    (b'\xd0\xb2\xd1\x9e\xe2\x80\xa2', b'\xe2\x9e\x95'),  # ➕
    # Minus sign
    (b'\xd0\xb2\xd1\x9e\xe2\x80\x93', b'\xe2\x9e\x96'),  # ➖
    # Check mark
    (b'\xd0\xb2\xd1\x9c\xe2\x80\xa6', b'\xe2\x9c\x85'),  # ✅
    # Question mark
    (b'\xd0\xb2\xd1\x9d\xe2\x80\x9d', b'\xe2\x9d\x93'),  # ❓
    # White circle
    (b'\xd0\xb2\xc5\xa1\xc2\xaa', b'\xe2\x9a\xaa'),  # ⚪
    # Horizontal line
    (b'\xd0\xb2\xe2\x80\x9e\xd0\x83', b'\xe2\x94\x83'),  # ┃
    # Dotted line
    (b'\xd0\xb2\xe2\x80\x9e\xe2\x80\x9e', b'\xe2\x94\x84'),  # ┄
]

total = 0
for old, new in fixes:
    count = raw.count(old)
    if count > 0:
        raw = raw.replace(old, new)
        total += count
        print(f"Fixed {count} occurrences")

with open('app/handlers.py', 'wb') as f:
    f.write(raw)

print(f"\nTotal fixes: {total}")

# Now check remaining mojibake
remaining = raw.count(b'\xd1\x80\xd1\x9f')
print(f"Remaining mojibake patterns: {remaining}")
